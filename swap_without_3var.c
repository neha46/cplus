#include<stdio.h>
int main()
{
    int num1,num2;
    printf("enter  number\n");
    scanf("%d%d",&num1,&num2);
    //before swaping
    printf("before swapping\n");
    printf("num1=%d\t num2=%d\n",num1,num2);
    //after swaping
    printf("after swapping\n");
    printf("num1=%d\t num2=%d\n",num2,num1);
    return 0;

}

